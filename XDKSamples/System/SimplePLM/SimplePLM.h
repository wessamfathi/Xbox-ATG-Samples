#pragma once

#include "DeviceResources.h"
#include "StepTimer.h"

#include "TextConsole.h"

// A basic sample implementation that creates a D3D11 device and
// provides a render loop.
class Sample
{
public:

    Sample() noexcept(false);

    // Initialization and management
    void Initialize(IUnknown* window);

    // Basic game loop
    void Tick();

    // Messages
    void OnSuspending();
    void OnResuming();

    //SampleFunctions
    void ShowInstructions();
    void LogPLMEvent(const wchar_t* primaryLog, const wchar_t* secondaryData = L"");
    bool GetUseDeferral() const { return m_useDeferral; }

private:

    void Update(DX::StepTimer const& timer);
    void Render();

    void Clear();

    void CreateDeviceDependentResources();
    void CreateWindowSizeDependentResources();

    // Device resources.
    std::unique_ptr<DX::DeviceResources>        m_deviceResources;

    // Rendering loop timer.
    uint64_t                                    m_frame;
    DX::StepTimer                               m_timer;

    // Input devices.
    std::unique_ptr<DirectX::GamePad>           m_gamePad;

    DirectX::GamePad::ButtonStateTracker        m_gamePadButtons;

    // DirectXTK objects.
    std::unique_ptr<DirectX::GraphicsMemory>    m_graphicsMemory;

    // Sample Specific
    std::unique_ptr<DX::TextConsoleImage>   m_console;
    bool                                    m_useDeferral;
    bool                                    m_consoleIsValid;
    std::vector<std::wstring>               m_logCache;
};
